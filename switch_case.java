public class switch_case{
	public static void main(String[] args){
		int a=4;
		switch (a){
			case 4:
				System.out.println("ui ti nua co ny");
				// break;
			case 5:
				System.out.println("Sap rui");
				// break;
			case 6:
				System.out.println("Da co");
				// break;
			default:
				System.out.println("KO coa dau");
				break;
		}
	}
}